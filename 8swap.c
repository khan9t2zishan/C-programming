//swap without an extra variable
#include<stdio.h>
void main()
{
    int a,b;
    printf("Inputs for a and b:");
    scanf("%d %d",&a,&b);
    printf("a=%d b=%d",b,a);
}

//Swap with an extra variable5
#include<stdio.h>
void main()
{
    int a,b,x,y;
    printf("a and b respectively are:");
    scanf("%d %d",&x,&y);
    printf("a=%d b=%d",y,x);


}